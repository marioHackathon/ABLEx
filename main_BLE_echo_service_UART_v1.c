/**@brief Function for handling the data from the Nordic UART Service.
 *
 * @details This function will process the data received from the Nordic UART BLE Service and send
 *          it to the UART module.
 *
 * @param[in] p_evt       Nordic UART Service event.
 */
/**@snippet [Handling the data received over BLE] */
static void nus_data_handler(ble_nus_evt_t * p_evt)
{

    if (p_evt->type == BLE_NUS_EVT_RX_DATA)
    {
        uint32_t err_code;

        NRF_LOG_DEBUG("Received data from BLE NUS. Writing data on UART.");
        NRF_LOG_HEXDUMP_DEBUG(p_evt->params.rx_data.p_data, p_evt->params.rx_data.length);

        for (uint32_t i = 0; i < p_evt->params.rx_data.length; i++)
        {
            do
            {
                err_code = app_uart_put(p_evt->params.rx_data.p_data[i]);       //Notifications by UART for debug actions.
                if ((err_code != NRF_SUCCESS) && (err_code != NRF_ERROR_BUSY))
                {
                    NRF_LOG_ERROR("Failed receiving NUS message. Error 0x%x. ", err_code);
                    APP_ERROR_CHECK(err_code);
                }
                
                //HASH (HMAC)
                  
                  uint16_t length = (uint16_t)p_evt->params.rx_data.length;
                  uint8_t *pdata = (uint8_t *)p_evt->params.rx_data.p_data;
                  uint8_t key[10]= "1234567890";
                  uint8_t dest = strcat(pdata, key);
                  uint8_t hash[32];

                  calc_sha_256(hash, dest, (length + 10));

                //---

                //Echo service 
                  uint16_t h_length = 32;
                  err_code = ble_nus_data_send(&m_nus, &hash, &h_length, m_conn_handle);

                //End service

            } while (err_code == NRF_ERROR_BUSY);
        }
        if (p_evt->params.rx_data.p_data[p_evt->params.rx_data.length - 1] == '\r')
        {
            while (app_uart_put('\n') == NRF_ERROR_BUSY);
        }
    }

}
